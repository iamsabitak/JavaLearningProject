public class Day1 {
    public static void main(String[] args) {
          // Declaring and initializing variables
          int age = 19;  // Integer type
          double salary = 45000.50;  // Double type (decimal)
          String name = "Sabita";  // String type (text)
          boolean isStudent = true;  // Boolean type (true or false)
  
          // Print the variables
          System.out.println("Name: " + name);
          System.out.println("Age: " + age);
          System.out.println("Salary: " + salary);
          System.out.println("Is a student: " + isStudent);

        // Item details
        String itemName = "Laptop";  
        int quantity = 2;  
        double pricePerItem = 55000.75;  

        double totalCost = quantity * pricePerItem;

        // Display the item details and total cost
        System.out.println("Item: " + itemName);
        System.out.println("Quantity: " + quantity);
        System.out.println("Price per item: $" + pricePerItem);
        System.out.println("Total cost: $" + totalCost);
         

    }
}
